
CREATE TABLE Products (
  id INTEGER NOT NULL DEFAULT 'NULL' PRIMARY KEY,
  Name VARCHAR(255) NOT NULL DEFAULT 'NULL',
  currency VARCHAR(5) NOT NULL DEFAULT 'USD',
  deadline DATETIME NOT NULL DEFAULT 'NULL',
);

CREATE TABLE Rewards (
  id INTEGER NOT NULL DEFAULT NULL PRIMARY KEY,
  productId INTEGER NOT NULL DEFAULT NULL,
  minimum INTEGER NOT NULL DEFAULT NULL,
  title VARCHAR(255) NOT NULL DEFAULT 'NULL',
  Description VARCHAR(255) NOT NULL DEFAULT 'NULL',
  includes VARCHAR(255) NOT NULL DEFAULT 'NULL',
  estDelivery DATE NOT NULL DEFAULT 'NULL',
  shipsTo VARCHAR(255) NOT NULL DEFAULT 'Anywhere in the world',
  deadline DATETIME NULL DEFAULT NULL,
  FOREIGN KEY (productId) REFERENCES Products (id)
);

CREATE TABLE Pledges (
  id INTEGER NOT NULL DEFAULT NULL PRIMARY KEY,
  productId INTEGER NOT NULL DEFAULT NULL,
  rewardId INTEGER NULL DEFAULT NULL,
  shipDest VARCHAR(255) NULL DEFAULT NULL,
  amount DECIMAL NOT NULL DEFAULT 0.00,
  FOREIGN KEY (productId) REFERENCES Products (id),
  FOREIGN KEY (rewardId) REFERENCES Rewards (id)
);